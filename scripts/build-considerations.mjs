// Generates packages/components/story-ui-considerations.md — the single-file
// design-system briefing that story-ui (https://github.com/southleft/story-ui)
// reads before generating Storybook stories.
//
// Deterministic, like the other generated artifacts in this repo: it derives
// everything from design-system.json + ai/rules.md, so the briefing can never
// drift from the source of truth. Re-run via `npm run build:considerations`
// (folded into `npm run build:meta`). Never hand-edit the output.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const ds = JSON.parse(readFileSync(join(root, 'design-system.json'), 'utf8'));
const rulesMd = readFileSync(join(root, 'ai', 'rules.md'), 'utf8');

// Pull just the "Hard Rules" block out of ai/rules.md so the briefing leads
// with the non-negotiables rather than restating the whole file.
const hardRules = (() => {
  const start = rulesMd.indexOf('## Hard Rules');
  if (start === -1) return '';
  const after = rulesMd.indexOf('\n## ', start + 1);
  return rulesMd.slice(start, after === -1 ? undefined : after).trim();
})();

function componentSection(c) {
  const lines = [`### \`<${c.name}>\``, '', c.summary, ''];

  if (c.props?.length) {
    lines.push('| Attribute | Type | Default | Notes |', '|---|---|---|---|');
    for (const p of c.props) {
      const type = String(p.type).replace(/\|/g, '\\|');
      const def = p.default === null ? '—' : `\`${p.default}\``;
      lines.push(`| \`${p.name}\` | ${type} | ${def} | ${p.description ?? ''} |`);
    }
    lines.push('');
  }

  if (c.slots?.length) {
    const slots = c.slots
      .map((s) => `${s.name === 'default' ? '(default)' : `\`${s.name}\``} — ${s.description}`)
      .join('; ');
    lines.push(`**Slots:** ${slots}`, '');
  }

  if (c.events?.length) {
    lines.push(`**Events:** ${c.events.map((e) => `\`${e.name}\``).join(', ')}`, '');
  }

  if (c.rules?.length) {
    lines.push('**Component rules:**');
    for (const r of c.rules) lines.push(`- ${r}`);
    lines.push('');
  }

  return lines.join('\n');
}

const components = [...ds.components].sort((a, b) => a.name.localeCompare(b.name));

const out = `<!-- GENERATED by scripts/build-considerations.mjs — do not edit by hand.
     Source of truth: design-system.json + ai/rules.md. Re-run \`npm run build:considerations\`. -->

# Story UI — Design System Considerations

This file briefs the AI on the **River Romney design system** so generated
Storybook stories use the real \`rr-*\` web components and stay on-token. It is
generated from \`design-system.json\` and \`ai/rules.md\`.

## How to use these components

- These are **framework-agnostic Lit web components** — author stories as HTML
  custom-element tags (e.g. \`<rr-button variant="primary">Save</rr-button>\`),
  not React/JSX.
- Register every element with a single **barrel side-effect import** at the top
  of each story file: \`import '@riverromney/components';\`. Do not deep-import
  individual component files — the package \`exports\` map only exposes the barrel.
- Style and theme come entirely from CSS custom properties on \`:root\` (loaded in
  \`.storybook/preview.ts\` from \`build/css/variables.css\`). Components inherit the
  base **dark theme** automatically — never set colors on the element.

## Non-negotiable rules

${hardRules}

> Generated stories that violate these are rejected by \`npm run validate\`
> (rules live in \`scripts/rules.mjs\`). Never emit hex literals, never reference
> \`--primitive-*\` tokens, and only use spacing/sizes from the token scale.

## Available components (${components.length})

${components.map(componentSection).join('\n')}
`;

const dest = join(root, 'packages', 'components', 'story-ui-considerations.md');
writeFileSync(dest, out);
console.log(`✔︎ wrote ${dest} (${components.length} components)`);
