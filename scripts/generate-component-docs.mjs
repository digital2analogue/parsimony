/**
 * generate-component-docs.mjs — code-first component DocGen.
 *
 * Reads design-system.json (component metadata + CEM-injected prop descriptions)
 * and the DTCG token store, and writes/patches one docs/components/<name>.mdx per
 * component. The generated MDX is host-portable (Mintlify now, Next.js+MDX later).
 *
 * Two kinds of region:
 *   - GEN     : regenerated every run from metadata. Overwritten; never hand-edit.
 *   - AUTHORED: written by a human (Overview, Do/Don't). Preserved across runs.
 * Regions are delimited by MDX comments so a regen never clobbers authored prose —
 * the same marker technique scripts/generate-docs.mjs uses for docs/index.html.
 *
 * Token values are resolved through scripts/tokens.mjs (the single source the MCP
 * and validate use) — never re-implemented here.
 *
 * Deterministic: no timestamps, stable component + token order, so CI can verify
 * freshness with `git diff --exit-code -- docs/components`.
 *
 * Usage: node scripts/generate-component-docs.mjs   (or: npm run docs:components)
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { resolve, relative } from "node:path";
import { fileURLToPath } from "node:url";
import { glob } from "node:fs/promises";
import { loadTokens, resolveToken, toCssVar } from "./tokens.mjs";

const ROOT = resolve(import.meta.dirname, "..");
const OUT_DIR = resolve(ROOT, "docs/components");
const REPO_BLOB = "https://github.com/digital2analogue/parsimony/blob/main";

// ── Formatting helpers (pure) ───────────────────────────────────────────────

/** rr-tab-list → "Tab List". */
export function titleCase(name) {
  return name
    .replace(/^rr-/, "")
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

/** Escape characters that would break a Markdown table cell. */
export function cell(s) {
  return String(s ?? "")
    .replace(/\|/g, "\\|")
    .replace(/\n+/g, " ")
    .trim();
}

/** Flatten a resolved token value (string, or composite object/array) to a
 *  human-readable one-liner. Composite typography/shadow → space-joined leaves. */
export function fmtVal(v) {
  if (v == null) return "";
  if (typeof v === "string") return v;
  if (Array.isArray(v)) return v.map(fmtVal).join(" ");
  if (typeof v === "object") return Object.values(v).map(fmtVal).join(" ");
  return String(v);
}

/** YAML double-quoted scalar (JSON string encoding is valid YAML). */
function yamlStr(s) {
  return JSON.stringify(String(s ?? ""));
}

// ── Region markers ──────────────────────────────────────────────────────────

const GEN_NOTE =
  "regenerated by scripts/generate-component-docs.mjs — do not hand-edit";

export function genRegion(id, body) {
  return `{/* GEN:${id}:start — ${GEN_NOTE} */}\n${body}\n{/* GEN:${id}:end */}`;
}

export function authoredRegion(id, body) {
  return `{/* AUTHORED:${id} — write freely; DocGen preserves this region */}\n${body}\n{/* /AUTHORED:${id} */}`;
}

/** Replace the body of an existing GEN region in `text`, preserving everything
 *  else (including AUTHORED regions). Appends the region if not present. */
export function patchGenRegion(text, id, rendered) {
  const re = new RegExp(
    `\\{/\\* GEN:${id}:start[\\s\\S]*?GEN:${id}:end \\*/\\}`,
  );
  if (re.test(text)) return text.replace(re, rendered);
  return `${text.replace(/\s*$/, "")}\n\n${rendered}\n`;
}

/** Replace the leading YAML front-matter block. */
export function patchFrontMatter(text, frontMatter) {
  const re = /^---\n[\s\S]*?\n---\n/;
  if (re.test(text)) return text.replace(re, `${frontMatter}\n`);
  return `${frontMatter}\n${text}`;
}

// ── Section renderers (pure) ────────────────────────────────────────────────

export function renderFrontMatter(c, { sourceUrl }) {
  const lines = [
    "---",
    `title: ${yamlStr(titleCase(c.name))}`,
    `component: ${c.name}`,
    `description: ${yamlStr(c.summary)}`,
    `package: ${yamlStr(c.package)}`,
    `status: ${c.status ?? "beta"}`,
  ];
  if (c.accessibility?.ariaPattern)
    lines.push(`a11yPattern: ${yamlStr(c.accessibility.ariaPattern)}`);
  if (c.figma?.nodeId) lines.push(`figmaNodeId: ${yamlStr(c.figma.nodeId)}`);
  if (sourceUrl) lines.push(`sourceUrl: ${yamlStr(sourceUrl)}`);
  lines.push("---");
  return lines.join("\n");
}

export function renderExamples(c) {
  const ex = c.examples ?? [];
  if (!ex.length) return "## Examples\n\n_No examples._";
  const blocks = ex.map(
    (e) => `### ${e.title}\n\n\`\`\`html\n${e.html}\n\`\`\``,
  );
  return `## Examples\n\n${blocks.join("\n\n")}`;
}

export function renderProps(c) {
  const props = c.props ?? [];
  if (!props.length) return "## Properties\n\n_No configurable properties._";
  const rows = props.map(
    (p) =>
      `| \`${cell(p.name)}\` | \`${cell(p.type)}\` | ${p.default === undefined ? "—" : `\`${cell(p.default)}\``} | ${cell(p.description)} |`,
  );
  return `## Properties\n\n| Prop | Type | Default | Description |\n|------|------|---------|-------------|\n${rows.join("\n")}`;
}

export function renderSlotsEvents(c) {
  const parts = [];
  if (c.slots?.length) {
    const rows = c.slots.map(
      (s) => `| \`${cell(s.name)}\` | ${cell(s.description)} |`,
    );
    parts.push(
      `### Slots\n\n| Slot | Description |\n|------|-------------|\n${rows.join("\n")}`,
    );
  }
  if (c.events?.length) {
    const rows = c.events.map(
      (e) => `| \`${cell(e.name)}\` | ${cell(e.detail)} |`,
    );
    parts.push(
      `### Events\n\n| Event | Detail |\n|-------|--------|\n${rows.join("\n")}`,
    );
  }
  const body = parts.length ? parts.join("\n\n") : "_No slots or events._";
  return `## Slots & Events\n\n${body}`;
}

export function renderTokenTable(title, entries) {
  if (!entries.length) return `## ${title}\n\n_None._`;
  const rows = entries.map(
    ([tok, val]) => `| \`${cell(tok)}\` | ${cell(val)} |`,
  );
  return `## ${title}\n\n| Token | Resolves to |\n|-------|-------------|\n${rows.join("\n")}`;
}

/** Split tokensUsed into typography (--font-*) and the rest, each resolved. */
export function resolveTokens(c, resolveCssVar) {
  const design = [];
  const typography = [];
  for (const v of c.tokensUsed ?? []) {
    const entry = [v, resolveCssVar(v)];
    (v.startsWith("--font-") ? typography : design).push(entry);
  }
  return { design, typography };
}

export function renderAccessibility(c) {
  const a = c.accessibility ?? {};
  const lines = [];
  if (a.ariaPattern)
    lines.push(
      `- **WAI-ARIA pattern:** [${a.ariaPattern.replace(/^https?:\/\//, "")}](${a.ariaPattern})`,
    );
  if (typeof a.focusable === "boolean")
    lines.push(`- **Focusable:** ${a.focusable ? "yes" : "no"}`);
  if (a.keyboard?.length)
    lines.push(`- **Keyboard:** ${a.keyboard.map((k) => cell(k)).join("; ")}`);
  if (a.wcag?.length)
    lines.push(`- **WCAG:** ${a.wcag.map((w) => cell(w)).join(", ")}`);
  const rules = (a.rules ?? []).map((r) => `- ${cell(r)}`);
  const body =
    (lines.length ? lines.join("\n") : "_No accessibility metadata._") +
    (rules.length ? `\n\n**Rules**\n\n${rules.join("\n")}` : "");
  return `## Accessibility\n\n${body}`;
}

export function renderGuardrails(c) {
  const rules = c.rules ?? [];
  if (!rules.length) return "## Guardrails\n\n_None._";
  return `## Guardrails\n\n${rules.map((r) => `- ${cell(r)}`).join("\n")}`;
}

export function renderImplementation(c) {
  const first = c.examples?.[0]?.html ?? `<${c.name}></${c.name}>`;
  return `## Implementation\n\nInstall the package and use the \`${c.name}\` custom element:\n\n\`\`\`bash\nnpm install ${c.package}\n\`\`\`\n\n\`\`\`html\n${first}\n\`\`\``;
}

// ── Assemble one component's regions ────────────────────────────────────────

export function buildRegions(c, { sourceUrl, resolveCssVar }) {
  const { design, typography } = resolveTokens(c, resolveCssVar);
  return {
    frontMatter: renderFrontMatter(c, { sourceUrl }),
    gen: {
      examples: renderExamples(c),
      properties: renderProps(c),
      "slots-events": renderSlotsEvents(c),
      tokens: renderTokenTable("Design tokens", design),
      typography: renderTokenTable("Typography", typography),
      accessibility: renderAccessibility(c),
      guardrails: renderGuardrails(c),
      implementation: renderImplementation(c),
    },
  };
}

/** Fresh page: authored stubs interleaved with GEN regions, in reading order. */
export function scaffoldDoc(c, { frontMatter, gen }) {
  const overview = authoredRegion(
    "overview",
    `## Overview\n\n_TODO: what is \`${c.name}\` for, and when to reach for it vs. an alternative._`,
  );
  const usage = authoredRegion(
    "usage",
    `## Usage\n\n_TODO: **Do** … / **Don't** … — the judgment guidance metadata can't derive._`,
  );
  const order = [
    frontMatter,
    overview,
    genRegion("examples", gen.examples),
    genRegion("properties", gen.properties),
    genRegion("slots-events", gen["slots-events"]),
    genRegion("tokens", gen.tokens),
    genRegion("typography", gen.typography),
    genRegion("accessibility", gen.accessibility),
    genRegion("guardrails", gen.guardrails),
    usage,
    genRegion("implementation", gen.implementation),
  ];
  return order.join("\n\n") + "\n";
}

/** Existing page: refresh front-matter + every GEN region; keep AUTHORED intact. */
export function patchDoc(existing, { frontMatter, gen }) {
  let out = patchFrontMatter(existing, frontMatter);
  for (const [id, body] of Object.entries(gen)) {
    out = patchGenRegion(out, id, genRegion(id, body));
  }
  return out;
}

// ── House-style exemplar ────────────────────────────────────────────────────

const HOUSE_STYLE = `---
title: House style
description: "How component docs in this folder are written and generated."
---

{/* This file is the exemplar every generated component doc matches — the
    code-first analog of the course's \`input.md\`. It is NOT auto-generated. */}

## How these docs work

Each \`docs/components/<name>.mdx\` is produced by \`npm run docs:components\`
(\`scripts/generate-component-docs.mjs\`) from \`design-system.json\` + the token store.

Two kinds of region:

- **GEN** (\`{/* GEN:<id>:start */}\` … \`{/* GEN:<id>:end */}\`) — regenerated
  every run from component metadata (props, tokens, a11y, examples, guardrails).
  **Never hand-edit** — your changes are overwritten. Fix the source
  (\`*.meta.json\`, per-property JSDoc, or the token \`$description\`) instead.
- **AUTHORED** (\`{/* AUTHORED:<id> */}\` … \`{/* /AUTHORED:<id> */}\`) — prose a
  human owns. DocGen preserves it across regens. Today that's **Overview** and
  **Usage (Do/Don't)** — the ~30% judgment layer.

## Voice

- Overview: one or two sentences — what it's for, and when to use it vs. a
  sibling (e.g. badge vs. tag vs. alert). Not a feature list.
- Usage: concrete **Do** / **Don't** pairs, tied to real misuse.
- Everything else is data — keep it in the metadata, not the prose.
`;

// ── Main ────────────────────────────────────────────────────────────────────

export async function main() {
  const ds = JSON.parse(
    readFileSync(resolve(ROOT, "design-system.json"), "utf8"),
  );
  const store = await loadTokens();

  // Reverse map: CSS custom property → dotted token path, for resolution.
  const cssToPath = new Map();
  for (const path of store.base.keys()) cssToPath.set(toCssVar(path), path);
  const resolveCssVar = (cssVar) => {
    const path = cssToPath.get(cssVar);
    if (!path) return "—";
    const t = resolveToken(store, path);
    return t ? fmtVal(t.value) : "—";
  };

  // component name → source .ts path (for the sourceUrl link).
  const srcByName = new Map();
  for await (const entry of glob("packages/components/**/*.meta.json", {
    cwd: ROOT,
  })) {
    const rel = relative(ROOT, resolve(ROOT, entry));
    const name = JSON.parse(readFileSync(resolve(ROOT, entry), "utf8")).name;
    srcByName.set(name, rel.replace(/\.meta\.json$/, ".ts"));
  }

  mkdirSync(OUT_DIR, { recursive: true });
  writeFileSync(resolve(OUT_DIR, "_house-style.mdx"), HOUSE_STYLE);

  const components = [...ds.components].sort((a, b) =>
    a.name < b.name ? -1 : a.name > b.name ? 1 : 0,
  );

  let created = 0;
  let patched = 0;
  for (const c of components) {
    const src = srcByName.get(c.name);
    const sourceUrl = src ? `${REPO_BLOB}/${src}` : null;
    const regions = buildRegions(c, { sourceUrl, resolveCssVar });
    const file = resolve(OUT_DIR, `${c.name}.mdx`);
    if (existsSync(file)) {
      const next = patchDoc(readFileSync(file, "utf8"), regions);
      writeFileSync(file, next);
      patched++;
    } else {
      writeFileSync(file, scaffoldDoc(c, regions));
      created++;
    }
    console.log(
      `  ${existsSync(file) ? "±" : "+"} docs/components/${c.name}.mdx`,
    );
  }

  console.log(
    `\n→ docs/components/ — ${created} created, ${patched} patched, ${components.length} total`,
  );
}

if (
  process.argv[1] &&
  fileURLToPath(import.meta.url) === resolve(process.argv[1])
) {
  await main();
}
